#!/bin/bash

# ((表达式)) 整数运算
((a = 5 + 2))
((b = a - 13))
((c = a + b))
echo "${a} ${b} ${c}"

a=$((5 / 4))
b=$((7 * 9))
c=$((2 ** 8))
d=$((75 % 23))
echo "${a} ${b} ${c} ${d}"

echo $((a == 1)) $((a > 1)) $((b == 63)) $((c < 300 && d > 5))

# 多个表达式之间用逗号分隔，以最后一个表达式的结果作为整个(( ))的值
echo $((35 + 7, 65536 / 2))

a=$((35 + 2 ** 5 * 10 - 9 % 4))
b=$(((12 + 35) * 3 / (5 - 3)))
echo $a $b
